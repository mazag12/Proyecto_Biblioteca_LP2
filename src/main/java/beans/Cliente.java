package beans;



public class Cliente {
	
	String codcliente;
	String codfamilia;
	String numero;
	String codtrabajo;
	String codestudio;
	String codperson;
	
	
	public String getCodcliente() {
		return codcliente;
	}
	public void setCodcliente(String codcliente) {
		this.codcliente = codcliente;
	}
	public String getCodfamilia() {
		return codfamilia;
	}
	public void setCodfamilia(String codfamilia) {
		this.codfamilia = codfamilia;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getCodtrabajo() {
		return codtrabajo;
	}
	public void setCodtrabajo(String codtrabajo) {
		this.codtrabajo = codtrabajo;
	}
	public String getCodestudio() {
		return codestudio;
	}
	public void setCodestudio(String codestudio) {
		this.codestudio = codestudio;
	}
	public String getCodperson() {
		return codperson;
	}
	public void setCodperson(String codperson) {
		this.codperson = codperson;
	}
	
	

}
