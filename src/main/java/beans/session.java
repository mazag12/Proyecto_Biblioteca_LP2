package beans;

public class session {
	public String dateSession;
	public String timeZoneSession;
	
	

	public String getDateSession() {
		return dateSession;
	}
	public void setDateSession(String dateSession) {
		this.dateSession = dateSession;
	}
	public String getTimeZoneSession() {
		return timeZoneSession;
	}
	public void setTimeZoneSession(String timeZoneSession) {
		this.timeZoneSession = timeZoneSession;
	}
}
